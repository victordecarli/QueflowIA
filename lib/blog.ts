export async function getBlogPost(slug: string) {
  const post = blogPosts[slug];
  if (!post) {
    throw new Error(`Artigo ${slug} não encontrado`);
  }
  return post;
}

export function getAllBlogPosts() {
  return Object.values(blogPosts).sort((a, b) => {
    if (a.date < b.date) {
      return 1;
    } else {
      return -1;
    }
  });
}
